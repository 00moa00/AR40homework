void ToUpper(const char* _Left, char* _Right)
{


	for (int num = 0; _Left[num] != 0; num++) {


		_Right[num] = _Left[num];

		if ('a' <= _Left[num] && _Left[num] <= 'z') {
			_Right[num] = _Left[num] - ('a' - 'A');
		}

	}

}


int ToInt(const char* _Left)
{
	int num = 0;
	bool type = 0; //부호 플러그

	//앞에 +가 붙어잇으면 무시한다.
	if (*_Left == '+') _Left++;

	//앞에 -가 있으면 type에 플러그 넣기
	else if (*_Left == '-') {
		type = true;
		_Left++;
	}

	while (*_Left != '/0') {
		//0~9이외의 문자열이라면 종료
		if (*_Left < 48 || *_Left > 57) break;

		num += *_Left - 48;
		num *= 10; //자릿수를 올려준다
		_Left++;
	}

	num /= 10; //루프에서 벗어나면 한자릿수 많기때문에 나눠주기

	//만약 -부호가 붙어있다면
	if (type) num = 0 - num;


	return num;

}

